--CREATE SCHEMA
DROP TABLE CUSTOMER_CARDS IF EXISTS CASCADE;
CREATE TABLE CUSTOMER_CARDS (
card_id INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
currentBalance DECIMAL(30, 2),
pin SMALLINT,
isLocked BOOLEAN,
FOREIGN KEY (transaction_id) REFERENCES CUSTOMER_TRANSACTIONS(cardTransaction_id));
PRIMARY KEY (card_id));

--CREATE SCHEMA
DROP TABLE CUSTOMER_TRANSACTIONS IF EXISTS CASCADE;
CREATE TABLE CUSTOMER_TRANSACTIONS (
cardTransaction_id bigint GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
cardTransactionCode VARCHAR(20),
cardTransactionDate DATE,
amount DECIMAL(30, 2),
PRIMARY KEY (cardTransaction_id));

--CREATE SCHEMA
DROP TABLE CUSTOMER IF EXISTS CASCADE;
CREATE TABLE CUSTOMER (
customer_id INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
customerName VARCHAR(30),
FOREIGN KEY (card_id) REFERENCES CUSTOMER_CARDS(card_id));
PRIMARY KEY (id));